      const next = cur === 'dark' ? 'light' : 'dark';
      apply(next);
      save(next);
    });
  }
  function sysChange(e){
    if(saved()) return;
    apply(e.matches ? 'dark' : 'light');
  }
  if(typeof prefersDark.addEventListener === 'function') prefersDark.addEventListener('change', sysChange);
  else if(typeof prefersDark.addListener === 'function') prefersDark.addListener(sysChange);
