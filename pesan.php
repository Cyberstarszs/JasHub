let selectedSubtype = null;

function renderTypes() {
  typeGrid.innerHTML = "";
  subtypeGrid.innerHTML = "";
  document.getElementById("service-subtype-title").style.display = "none";

  if (!selectedPlatform) return;
  Object.keys(pricingData[selectedPlatform]).forEach(type => {
    const card = document.createElement("div");
    card.className = "option-card";
    card.innerHTML = `<span>${type}</span>`;
    card.onclick = () => {
      selectedType = type;
      selectedSubtype = null;
      selectedPackage = null;
      highlightSelection(typeGrid, card);

      if (selectedPlatform === "Kuota") {
        renderSubtypes(); // render level 2 (jenis kuota)
      } else {
        renderPackages();
      }

      priceDisplay.textContent = "";
      accountLinkGroup.style.display = "none";
      videoLinkGroup.style.display = "none";
      phoneNumberGroup.style.display = "none";
    };
    typeGrid.appendChild(card);
  });
}

function renderSubtypes() {
  subtypeGrid.innerHTML = "";
  document.getElementById("service-subtype-title").style.display = "block";

  if (!selectedPlatform || !selectedType) return;
  Object.keys(pricingData[selectedPlatform][selectedType]).forEach(sub => {
    const card = document.createElement("div");
    card.className = "option-card";
    card.innerHTML = `<span>${sub}</span>`;
    card.onclick = () => {
      selectedSubtype = sub;
      selectedPackage = null;
      highlightSelection(subtypeGrid, card);
      renderPackages();
      priceDisplay.textContent = "";
      phoneNumberGroup.style.display = "none";
    };
    subtypeGrid.appendChild(card);
  });
}

function renderPackages() {
  packageGrid.innerHTML = "";
  if (!selectedPlatform) return;

  let packages = [];
  if (selectedPlatform === "Kuota") {
    if (!selectedType || !selectedSubtype) return;
    packages = pricingData[selectedPlatform][selectedType][selectedSubtype];
  } else {
    if (!selectedType) return;
    packages = pricingData[selectedPlatform][selectedType];
  }

  packages.forEach(pkg => {
    const card = document.createElement("div");
    card.className = "option-card";
    card.innerHTML = `
      <span>${pkg.quantity}</span>
      <div style="margin-top:6px;font-weight:700;color:#00c6ff;">Rp ${parseInt(pkg.price).toLocaleString("id-ID")}</div>
    `;
    card.onclick = () => {
      selectedPackage = pkg;
      highlightSelection(packageGrid, card);
      priceDisplay.textContent = `Harga: Rp ${parseInt(pkg.price).toLocaleString("id-ID")}`;

      if (selectedPlatform === "Kuota") {
        phoneNumberGroup.style.display = "block";
      }
    };
    packageGrid.appendChild(card);
  });
}
