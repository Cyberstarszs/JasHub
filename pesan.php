async function getLastTransactionId() {
  try {
    const response = await fetch('hasil_transaksi.json');
    const transactions = await response.json();
    if (transactions.length === 0) {
      return "0001";
    }
    const sortedTransactions = transactions.sort((a, b) => parseInt(b.ID) - parseInt(a.ID));
    const lastId = parseInt(sortedTransactions[0].ID);
    return (lastId + 1).toString().padStart(4, '0');
  } catch (error) {
    console.error('Error loading transaction data:', error);
    return new Date().getTime().toString().slice(-4);
  }
}

